def haroon(a, b, op):

    if op == "+":
        return a + b

    elif op == "-":
        return a - b

    elif op == "*":
        return a * b

    elif op == "/":
        return a / b


print(haroon(12, 43, "+"))
print(haroon(12, 43, "-"))
print(haroon(12, 43, "*"))
